def build_messages(stock, 작성일, 주주, text, price_ctx):
    msgs = [{"role": "system", "content": SYSTEM}]
    for u, a in FEWSHOT:                       # few-shot
        msgs.append({"role": "user", "content": u})
        msgs.append({"role": "assistant", "content": json.dumps(a, ensure_ascii=False)})
    holder = "주주" if 주주 else "비주주"
    date = str(작성일)[:10]
    user = (f"[종목] {stock}\n{price_ctx}\n작성일 {date} / {holder} / \"{text}\"")
    msgs.append({"role": "user", "content": user})
    return msgs


