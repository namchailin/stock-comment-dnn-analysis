def _load_grounding() -> str:
    """domain_grounding.md 의 INJECT 블록만 추출(단일 출처)."""
    with open(os.path.join(_HERE, "domain_grounding.md"), encoding="utf-8") as f:
        t = f.read()
    return t.split("<!-- INJECT:START -->")[1].split("<!-- INJECT:END -->")[0].strip()


